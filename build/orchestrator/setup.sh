#!/bin/bash
# One-time setup: collect your SSH public keys and write them to orchestrator.env
# so the orchestrator container allows you in via SSH.
#
# Run once (or re-run after adding new keys):
#   bash /ai/build/orchestrator/setup.sh

set -e

ENV_FILE="$(dirname "$0")/orchestrator.env"
SSH_DIR="${HOME}/.ssh"

# Collect all public keys in ~/.ssh/
KEYS=""
for pub in "$SSH_DIR"/*.pub; do
    [[ -f "$pub" ]] || continue
    key=$(cat "$pub")
    echo "  Found: $pub"
    KEYS="${KEYS}${key}"$'\n'
done

if [[ -z "$KEYS" ]]; then
    echo "No .pub files found in $SSH_DIR" >&2
    exit 1
fi

# Write gitignored env file (single-line, newlines escaped for env var)
KEYS_ONELINE=$(echo "$KEYS" | sed '/^$/d' | paste -sd '\\n' -)

cat > "$ENV_FILE" <<EOF
# Generated by setup.sh — do not commit
AUTHORIZED_KEYS=${KEYS_ONELINE}
EOF

echo ""
echo "Written to $ENV_FILE"
echo "Now run: docker compose -f /ai/compose/orchestrator.yaml up -d --build"
